using System;

namespace AK_Game
{
    //-----------------------------------------------------------------------------
    // GameEntity
    //
    // Base class for all classes in the game
    //-----------------------------------------------------------------------------
    public class GameEntity
    {
        protected EntityID mID;

        //-----------------------------------------------------------------------------
        //
        //-----------------------------------------------------------------------------
        public GameEntity(EntityID id)
        {
            mID = id;
        }

        //-----------------------------------------------------------------------------
        //
        //-----------------------------------------------------------------------------
        public EntityID GetID()
        {
            return mID;
        }

        //-----------------------------------------------------------------------------
        //
        //-----------------------------------------------------------------------------
        public void SetID(EntityID id)
        {
            mID = id;
        }

        //-----------------------------------------------------------------------------
        //
        //-----------------------------------------------------------------------------
        public virtual void Update(TimeSpan currentGameTime)
        {
        }
    }
}
